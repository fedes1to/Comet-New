typedef void (*CommandFunction)();

HINSTANCE hDll;

void LoadFunctions() {
	hDll = LoadLibrary("IpConfigCommands.dll");
}

void UnloadFunctions() {
	if (hDll) {
		FreeLibrary(hDll);
	}
}

CommandFunction GetFunction(const char* funcName) {
	return (CommandFunction)GetProcAddress(hDll, funcName); 
}











